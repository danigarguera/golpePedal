import { Injectable } from '@angular/core';
import { CanActivate, Router } from '@angular/router';

@Injectable({
  providedIn: 'root'
})
export class AuthGuard implements CanActivate {

  constructor(private router: Router) {}

  canActivate(): boolean {
    const token = localStorage.getItem('token');
    console.log('üõ°Ô∏è Comprobando token en AuthGuard:', token);
  
    if (token) {
      console.log('‚úÖ Token encontrado. Acceso permitido.');
      return true;
    } else {
      console.log('‚ùå Token no encontrado. Redirigiendo a login.');
      this.router.navigate(['/login']);
      return false;
    }
  }
  
}
